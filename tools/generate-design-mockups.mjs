function esc(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function svg(title, body) {
  return `<?xml version="1.0" encoding="UTF-8"?>
<svg width="${W}" height="${H}" viewBox="0 0 ${W} ${H}" fill="none" xmlns="http://www.w3.org/2000/svg">
  <title>${esc(title)}</title>
  <rect width="${W}" height="${H}" fill="#F5F7FA"/>
  ${body}
</svg>
`;
}

const t = (x, y, text, size = 18, weight = 400, fill = "#172033", anchor = "start") =>
  `<text x="${x}" y="${y}" font-family="Inter, Segoe UI, Arial, sans-serif" font-size="${size}" font-weight="${weight}" fill="${fill}" text-anchor="${anchor}">${esc(text)}</text>`;
const r = (x, y, w, h, fill = "#fff", stroke = "#DDE4EE", rx = 6) =>
  `<rect x="${x}" y="${y}" width="${w}" height="${h}" rx="${rx}" fill="${fill}" stroke="${stroke}"/>`;
const line = (x1, y1, x2, y2, stroke = "#E6ECF4") =>
  `<line x1="${x1}" y1="${y1}" x2="${x2}" y2="${y2}" stroke="${stroke}"/>`;
const pill = (x, y, w, text, fill, color) =>
  `${r(x, y, w, 28, fill, "none", 14)}${t(x + w / 2, y + 19, text, 13, 600, color, "middle")}`;
const btn = (x, y, w, text, primary = true) =>
  `${r(x, y, w, 42, primary ? "#2563EB" : "#FFFFFF", primary ? "#2563EB" : "#D6DEE9", 6)}${t(x + w / 2, y + 27, text, 15, 700, primary ? "#FFFFFF" : "#2D3B50", "middle")}`;
