  `${t(x, y, label, 14, 600, "#36445A")}${r(x, y + 12, w, 44, "#FFFFFF", "#D8E1EC", 6)}${value ? t(x + 14, y + 41, value, 14, 400, "#66758B") : ""}`;

function sidebar(active = "Главная", role = "HR") {
  const items = role === "Директор"
    ? ["Главная", "Документы", "Подпись"]
    : ["Главная", "Сотрудники", "Документы", "Шаблоны"];
  let y = 182;
  const rows = items.map((item) => {
    const selected = item === active;
    const row = `${r(24, y - 24, 212, 44, selected ? "#EAF1FF" : "#FFFFFF", selected ? "#BDD0FF" : "#FFFFFF", 6)}
      <circle cx="48" cy="${y - 2}" r="8" fill="${selected ? "#2563EB" : "#A7B4C6"}"/>
      ${t(68, y + 4, item, 15, selected ? 700 : 500, selected ? "#1D4ED8" : "#526174")}`;
    y += 54;
    return row;
  }).join("\n");
  return `
    <rect x="0" y="0" width="260" height="${H}" fill="#FFFFFF"/>
    ${line(260, 0, 260, H, "#E0E6EF")}
    <rect x="28" y="34" width="46" height="46" rx="10" fill="#EAF1FF" stroke="#BDD0FF"/>
    <path d="M52 46 L63 51 V61 C63 68 57 72 52 74 C47 72 41 68 41 61 V51 L52 46Z" stroke="#2563EB" stroke-width="3" fill="none"/>
    ${t(86, 56, "HR Docs", 22, 800, "#172033")}
    ${t(86, 78, role, 13, 600, "#6A7890")}
    ${rows}
    <rect x="24" y="850" width="212" height="70" rx="8" fill="#F7F9FC" stroke="#E4EAF2"/>
    ${t(46, 880, role === "Директор" ? "director" : "hr", 15, 700, "#172033")}
    ${t(46, 904, role === "Директор" ? "Подписание документов" : "HR-отдел", 13, 400, "#6A7890")}
  `;
}

