function statCard(x, y, label, value, color) {
  return `
    ${r(x, y, 244, 116, "#FFFFFF", "#DDE4EE", 8)}
    <rect x="${x + 20}" y="${y + 20}" width="44" height="44" rx="10" fill="${color}22"/>
    <circle cx="${x + 42}" cy="${y + 42}" r="10" fill="${color}"/>
    ${t(x + 82, y + 43, value, 28, 800, "#172033")}
    ${t(x + 82, y + 70, label, 14, 500, "#66758B")}
  `;
}

function table(x, y, widths, headers, rows) {
  const total = widths.reduce((a, b) => a + b, 0);
  let out = `${r(x, y, total, 56 + rows.length * 54, "#FFFFFF", "#DDE4EE", 8)}
    <rect x="${x}" y="${y}" width="${total}" height="56" rx="8" fill="#F8FAFD"/>
    ${line(x, y + 56, x + total, y + 56)}`;
  let cx = x;
  headers.forEach((h, i) => {
    out += t(cx + 18, y + 35, h, 13, 700, "#5C6A7D");
    cx += widths[i];
  });
  rows.forEach((row, ri) => {
    const ry = y + 56 + ri * 54;
    out += line(x, ry + 54, x + total, ry + 54);
    cx = x;
    row.forEach((cell, ci) => {
      out += typeof cell === "string"
        ? t(cx + 18, ry + 34, cell, 14, 500, ci === 0 ? "#172033" : "#526174")
        : cell(cx + 18, ry + 14);
      cx += widths[ci];
    });
  });
  return out;
}

